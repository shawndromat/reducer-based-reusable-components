export const setKhaleesiMode = khaleesiMode => {
    return {
        type: 'SET_KHALEESI_MODE',
        khaleesiMode
    }
}

export const addDragon = dragon => {
    return {
        type: 'ADD_DRAGON',
        dragon
    }
}

export const setDragonFire = dragonFire => {
    return {
        type: 'SET_DRAGON_FIRE',
        dragonFire
    }
}